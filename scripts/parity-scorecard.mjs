import { mkdir, readdir, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const FRAMEWORKS = ["react", "vue", "svelte", "angular", "lit"];

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const repoRoot = path.resolve(__dirname, "..");
const generatedRoot = path.join(repoRoot, "generated");
const reportDir = path.join(generatedRoot, ".foundry", "reports");
const reportPath = path.join(reportDir, "framework-parity-scorecard.md");

function percentage(value) {
  return `${(value * 100).toFixed(1)}%`;
}

async function listComponentDirs(framework) {
  const dir = path.join(generatedRoot, framework);
  const entries = await readdir(dir, { withFileTypes: true });
  return entries.filter((entry) => entry.isDirectory()).map((entry) => entry.name);
}

async function countFiles(framework, component) {
  const dir = path.join(generatedRoot, framework, component);
  const entries = await readdir(dir, { withFileTypes: true });
  return entries.filter((entry) => entry.isFile()).length;
}

async function buildScorecard() {
  const frameworkComponents = new Map();

  for (const framework of FRAMEWORKS) {
    frameworkComponents.set(framework, await listComponentDirs(framework));
  }

  const allComponents = new Set();
  for (const framework of FRAMEWORKS) {
    for (const component of frameworkComponents.get(framework) ?? []) {
      allComponents.add(component);
    }
  }

  const sortedComponents = [...allComponents].sort((a, b) => a.localeCompare(b));

  let fullyCovered = 0;
  const rowLines = [];

  for (const component of sortedComponents) {
    const presences = [];
    const filesByFramework = [];

    for (const framework of FRAMEWORKS) {
      const hasComponent = (frameworkComponents.get(framework) ?? []).includes(component);
      presences.push(hasComponent);

      if (hasComponent) {
        const files = await countFiles(framework, component);
        filesByFramework.push(`${framework}:${files}`);
      } else {
        filesByFramework.push(`${framework}:0`);
      }
    }

    const covered = presences.filter(Boolean).length;
    const coverage = covered / FRAMEWORKS.length;

    if (coverage === 1) {
      fullyCovered += 1;
    }

    const status = coverage === 1 ? "✅" : coverage >= 0.8 ? "⚠️" : "❌";
    const missing = FRAMEWORKS.filter((_, index) => !presences[index]);
    const missingText = missing.length === 0 ? "-" : missing.join(", ");

    rowLines.push(
      `| ${component} | ${status} | ${percentage(coverage)} | ${filesByFramework.join(" \\| ")} | ${missingText} |`,
    );
  }

  const overallCoverage =
    sortedComponents.length === 0 ? 0 : fullyCovered / sortedComponents.length;

  const markdown = `# Framework Parity Scorecard\n\nGenerated by \`scripts/parity-scorecard.mjs\`.\n\n## Summary\n\n- Total components: **${sortedComponents.length}**\n- Fully covered components (all frameworks): **${fullyCovered}**\n- Overall full-parity ratio: **${percentage(overallCoverage)}**\n\nFrameworks evaluated: ${FRAMEWORKS.join(", ")}\n\n## Per-component parity\n\n| Component | Status | Coverage | Files (react\|vue\|svelte\|angular\|lit) | Missing frameworks |\n|---|---:|---:|---|---|\n${rowLines.join("\n")}\n\n## Status key\n\n- ✅ 100% framework coverage\n- ⚠️ Partial coverage (>=80%)\n- ❌ Coverage below 80%\n`;

  await mkdir(reportDir, { recursive: true });
  await writeFile(reportPath, markdown, "utf8");

  console.log(`Wrote parity scorecard: ${path.relative(repoRoot, reportPath)}`);
}

await buildScorecard();
